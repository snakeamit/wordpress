		          <h4><strong>Amount of foreign currency one can take abroad on private visit:</strong></h4>
                  <ul>
                    <li>Any resident India can take up to USD 250000 or equivalent in a financial year under <a href="https://rbi.org.in/scripts/notificationuser.aspx?id=10192#1" target="_blank">liberalized remittance</a> scheme by RBI. USD 250000 as on 15.08.2021 are equivalent to Rs. 1,85,61,500.</li> 
                    <li>There is no limit on number of travels but in any case, the limit should not exceed USD 250000 per financial year.</li>
                    <li>The limit is available for individuals and not for a family. So, if there are four members in a family then each member can take USD 250000 per visit, but this limit can not be combined. </li>
                    <li>Foreign currency notes and coins only up to USD 3000 can be carried by an individual on per visit and the rest amount for USD 247000 can be carried in the form of Drafts, Store value cards, travel currency cards and travelers’ cheque. Use real time <a href="https://ibrlive.com/home">currency calculator</a> to get INR value.</li>
                    <li>International credit cards and debit cards can also be used abroad under the total limit of USD 250000 per year.</li>
                    <li>The air tickets bought in India in INR for travel to any foreign country will be a part of the individual’s overall limit of USD 250000/-.</li>
                    <li>Currency notes up to Rs. 50000 can be purchased in cash and balance can be purchased through Cheque, RTGS or bank transfer.</li>
                    <li>For traveling to Nepal and Bhutan one can carry the currency notes of Rs. 100 and below denominations up to any limit. One cannot carry Rs. 500 and Rs 2000 notes for more than Rs. 25000.</li>
                    <li>Travelers returning back to India from a foreign trip need to surrender the unspent foreign currency notes and travelers’ cheque within 180 days of return date, but they can retain USD 2000 only for the purpose of next visit.</li>
                    <li>No need to surrender coins. They can be retained forever.</li>
                  </ul>  
             
                  <h4><strong>Amount of foreign currency one can take abroad on Business Visit:</strong></h4>
                  <ul>
                    <li>For business travel to foreign countries, resident Indians can take up to USD 250000 in a financial year.</li>
                    <li>There is no limit on number of travels but in any case, the limit should not exceed USD 250000 per financial year. This limit can be exhausted in a single visit also.</li>
                    <li>Foreign currency notes and coins only up to USD 3000 can be carried by an individual on per visit and the rest amount for USD 247000 can be carried in the form of Drafts, Store value cards, travel currency cards and travelers’ cheque.</li>
                  </ul>
                  
                  <h4><strong>Amount of foreign currency one can take for studying abroad:</strong></h4>
                  <ul>
                    <li>For study abroad, one can remit up to USD 250000 in a financial year under LRS scheme.</li>
                    <li>This limit includes the following expenses:
                        <ol type="a">
                        <li>Remittance to universities or colleges for education fees and hostel fees.</li>
                        <li>Expenses for food, stay and other personal expenses.</li>
                        <li>Tickets booked for travelling abroad from India.</li>
                        </ol>
                    </li>
                    <li>If the course fee is more than USD 250000, then Authorized Dealers (Banks & FFMCs) may permit the remittance over and above USD 250000 without prior approval of RBI, based on the evidence provided.</li>
