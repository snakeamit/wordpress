		          <h4><strong>What is the Fx Retail platform?</strong></h4>
		          <p>It is a platform developed by Clear Corp Dealing Systems India Pvt Ltd. a subsidiary of Clearing Corporation of India Limited in association with <a href="https://www.rbi.org.in/Scripts/NotificationUser.aspx?Id=11597&Mode=0" target="_blank">RBI</a>. It was launched on 1st July 2019 and started doing transactions on August 5th, 2019. The platform provides sale & purchase of USD/INR pair by retail customers of banks for delivery-based Cash, TOM, SPOT, and Forward transactions up to 13 months.</p>
		          
		          <h4><strong>Why was the CCIL Fx Retail platform created?</strong></h4>
		          <p>Due to the non-transparent currency conversion system of the banks, clients were paying very heavy exchange margins on their currency conversions and approaching RBI to make the process transparent. On consecutive requests from the clients, RBI decided to make a transparent currency conversion process and hence created the Fx retail platform.</p>
		          
		          <h4><strong>Who can benefit from the platform?</strong></h4>
		          <p>Any customer of the bank be it an Individual, NRI, Proprietorship firm, Partnership firm, Private Limited Company, Limited Company, LLP, Exporter, or Importer can use the system for their legitimate foreign exchange transactions allowed by RBI.</p>
		          
		          <h4><strong>Which currency can be traded on this platform?</strong></h4>
		          <p>US Dollar accounts for more than 85% of cross-border transactions. Hence, the platform only provides to transact in USD/INR pair.</p>
		          
		          <h4><strong>What kind of transactions are allowed by Fx Retail?</strong></h4>
                  <p>Buy or Sell USD/INR for Cash Delivery (Today), TOM Delivery (T+1) or SPOT Delivery (T+2) can be done on this platform. Users can also book and <a href="https://ibrlive.com/blogs/currency-forward-contract-definition-booking-cancellation" target="_blank">cancel forward contracts</a> on this platform for up to 13 months of delivery.</p>
                  
                  <h4><strong>What are the charges and daily limits for transactions?</strong></h4>
                  <p>There are no charges for booking rates up to USD 50000 per day, but after this limit, a charge of 0.0004% is levied on the total amount. There is no limit on the number of transactions per day but a single transaction cannot exceed 5 million Dollars.</p>
                  
                  <h4><strong>How many bank accounts can be registered?</strong></h4>
                  <p>There are around 56 banks available on this platform. A user can register more than one bank account on this platform and choose to transact with the bank thatâ€™s taking the least margin from them.</p>
                  
                  <h4><strong>How to register?</strong></h4>
                  <ul>
                    <li>Log on to <a href="https://www.fxretail.co.in" target="_blank">https://www.fxretail.co.in</a></li>  
                    <li>Click on the option "register here"</li> 
                    <li>Fill in all your details like customer type, GST No., LEI No., address, phone number, etc.</li> 
                    <li>Select your bank from the list and fill in bank details like account no., IFSC code, and branch name.</li> 
                    <li>Submit the application for registration with your bank.</li> 
                    <li>Get the ID & Password from your bank on your registered email ID.</li> 
                  </ul>  
                  <p>If you find it difficult to complete the process, <a href="https://ibrlive.com">IBR Live</a> can help you complete the process and negotiate for a better margin with your bank.</p>
                  
                  <h4><strong>What are the advantages of using this for import-export transactions?</strong></h4>
		          <ol type="a">
                    <li>Transparent system- The margin on currency conversion taken by your bank is displayed to you. Hence there is no conflict between you and the bank on currency conversion.</li>
                    <li>Cost-saving- By registering multiple banks on this platform you can compare the markups charged by your bank and you can transact with the bank that is quoting the best rate.</li>
                    <li>Placing orders for the day- Day orders can be placed at a price at which the customer is comfortable to book the rate. If the rate comes then the deals get executed and if not, then the order gets canceled itself.</li>
