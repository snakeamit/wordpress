		          <h4><strong>Benefits of LEI Code to global financial institutions:</strong></h4>
		          <ol type="a">
                    <li>To simplify, standardize and provide unique identification to any legal entity globally.</li>
                    <li>LEI ensures to have one standard system for operational purposes worldwide.</li>
                    <li>According to RBI point of view, LEI helps in reducing the financial crisis in India.</li>
                    <li>It helps in improving transparency in financial data systems.</li>
                    <li>It prevents bank and credit providers from corporate borrowers from issuing the multiple loans against the same collateral by monitoring their exposures.</li>
                  </ol>  
                  <br/>
                  
                  <h4><strong>Benefits of LEI Code to business entities:</strong></h4>
		          <ol type="a">
                    <li>Provides a legal existence and unique identification to any company or entity.</li>
                    <li>Increased transparency in financial markets.</li>
                    <li>LEI Certificate helps in international business.</li>
                    <li>Increased trading credibility.</li>
                    <li>It gives benefit of security because the LEI data helps the customers, investors and stakeholders to find who they are dealing with.</li>
                  </ol> 
