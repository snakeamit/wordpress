		          <h4><strong>Evolution of Legal Entity Identifier</strong></h4>
	
		          <p>During 2007-2008, it was the time of financial crisis. So, the regulators realized that it was difficult to estimate the amount of risk exposure in the market. It became very hard to resolve the problems of failing institutions in the market. A single identification code which is unique to each financial institution was not available throughout the world. Therefore, a need of unique code for each country was recognized. </p>
		          <p>Then the LEI system came into existence by 2011 so that the organisations could be identified uniquely and their financial transactions could be tracked easily.</p>
		          <p>The first LEIs were developed in December 2012. Currently, there have been over 2 million LEIs created globally.</p>
		          <br/>
		          
		          <h4><strong>What is Legal Entity Identifier?</strong></h4>
		          <p>The Legal Entity Identifier is a unique identification code consists of 20 characters which is standardized with ISO certification 17442. Each LEI code contains details about ownership structure of an entity and provides the information about who manages whom and what is the constitution and address of the entity. Thus, the LEI data pool can be referred as a global directory, which enhances transparency in the global marketplace. Now it has become a legal requirement for many companies within the global financial system to have an LEI code. Many entities may have similar or same name. LEI Code gives unique identity to each company so they easily searched by their LEI Number.</p>
		          <br/>
		          
		          <h4><strong>LEI Code Structure</strong></h4>
		          <p>LEI Code consists 20 characters which is standardized with ISO certificate 17442 by the International Organization for Standardization (ISO). </p>
		          <ol type="a">
                    <li>First 4 characters identify the Local Operating Unit (LOU) that issued the LEI</li>
                    <li>Character 5 and 6 always have 0 value.</li>
                    <li>Characters 7 to 18 have unique value for each entity.</li>
                    <li>Character 19 and 20 are checksum digits for verification purposes</li>
                  </ol>
                  <br/>
                    
		          <h4><strong>Is it mandatory to take LEI code in India?</strong></h4>
		          <ol type="I">
                    <li>Borrowers who fail to acquire LEI codes from an authorized Local Operating Unit (LOU) as per the time lines mentioned below will not be permitted to take any kind of enhancement or renewal of his existing exposure/loan.</li>
                    <li>No entity can book forward contracts from the banks without having LEI code.</li>
                    <li>Departments of Central and State Governments (not Public Sector Undertakings registered under Companies Act or established as Corporation under the relevant statute) are exempted from this provision.</li>
                  </ol>
                  <br/>
                  
