		          <h4><strong>What is OFAC?</strong></h4>
	
		          <p>OFAC stands for Office of Foreign Assets Control. It is administrated by the Treasury Department of the United States. OFAC carries out economic sanctions (penalty or rules) which are imposed by the U.S on Foreign Countries. The program may freeze assets of sanctioned countries and may restrict services to sanctioned countries. It can also restrict payment of funds to the person or entities named in the SDN list (Specially Designated Nationals and Blocked Persons List).</p>
		          <br/>
		          <h4><strong>Types of OFAC Sanctions</strong></h4> 
		          <h4><strong>1. Comprehensive Sanctions</strong></h4>
		          <p>When sanctions are imposed on the entire country, it is called a comprehensive sanction or country-based sanction. There can be restrictions on imports, exports, <a href="https://ibrlive.com/blogs/usd-to-inr-exchange-rate-by-settlement-date">financial transactions</a>, investment, and travel in these regions. </p>
		          
		          <p>At present there are five comprehensive sanctioned countries named below:</p>
		          <ol type="i">
                    <li>Crimea region of Ukraine: Restriction on imports, exports, and investment.</li>
                    <li>Cuba: Restriction on imports, exports, financial transactions, and travel.</li>
                    <li>Iran: Restriction on imports, exports, and financial transactions.</li>
                    <li>North Korea: Restriction on imports, exports, and travel.</li>
                    <li>Syria: Restriction on imports, exports, and financial transactions.</li>
                  </ol>
                  
		          <p>Recently, Sudan and Myanmar were removed from the list of sanctioned countries.</p>
		          <br/>
		          
		          <h4><strong>2. Non-Comprehensive Sanctions</strong></h4>
		          <p>When sanctions are imposed on a specified area or entity or persons rather than the large geography or country it is called non-comprehensive sanctions. A list of the persons, entities, or areas is maintained and updated from time to time. Dealing with the countries mentioned in the SDN list is prohibited but you can deal with the countries or persons other than those mentioned in the SDN list. Non-comprehensive sanctions program also carries out activities like drug trafficking and terrorism. At present, the following countries are covered under non-comprehensive sanctions programs, with <a href="https://ibrlive.com/blogs/foreign-currency-abroad-for-travel-business-education-purpose">restrictions</a> on activities with specified parties.</p>
		          <ol type="i">
                    <li>Balkans</li>
                    <li>Belarus</li>
                    <li>Burma (Myanmar)</li>
                    <li>Burundi</li>
                    <li>Central African Republic</li>
                    <li>The Democratic Republic of the Congo</li>
                    <li>Lebanon</li>
                    <li>Libya</li>
                    <li>Russia/Ukraine: Restrictions on activities with financial/energy sectors.</li>
                    <li>Somalia</li>
                    <li>South Sudan</li>
                    <li>Venezuela</li>
                    <li>Yemen</li>
                    <li>Zimbabwe</li>
                  </ol>    
                  <p>Please note that Sanctions programs are changed frequently, hence these should be verified from the <a target="_blank" href="https://home.treasury.gov/policy-issues/financial-sanctions/sanctions-programs-and-country-information">official website</a> of the Department of Treasury.</p>
                  <br/>
                  
		          <p><strong>Tips for Importers-Exporters dealing with OFAC countries</strong></p>
                  <ol type="i">
                    <li>You should <b>not trade</b> with sanctioned countries mentioned in comprehensive sanctions programs as an importer or exporter of goods & services.</li>
                    <li>You <b>can trade</b> with the countries mentioned in the non-comprehensive sanctions list mentioned above but check the updated <a href="https://home.treasury.gov/policy-issues/financial-sanctions/specially-designated-nationals-and-blocked-persons-list-sdn-human-readable-lists" target="_blank">SDN list</a> before dealing with persons or entities in the countries named in the non-comprehensive sanctions list.</li>
                    <li>If you have accidentally dealt with the countries mentioned in the comprehensive sanctions list, your banker may refuse to settle the proceeds of such trade or he can refuse to settle the Shipping Bill or Bill of Entries which will remain pending in your record.</li>
                  </ol>    
