		          <p>Booking a <a href="https://ibrlive.com/blogs/currency-forward-contract-definition-booking-cancellation">currency forward</a> contract hedge your adverse currency movement risk, but by adopting the following strategies, you may earn good profits out of it.</p><br/>
		          
		          
		          <p><strong>1) Always book partial exposure: :</strong> Booking a partial exposure is always a good idea. For example, if you expect an inward payment of USD 100000, you may book 50% of your exposure, i.e USD 50000, and keep USD 50000 open. This strategy will balance your currency exchange rate. If the current spot rate on the payment date is above your booking rate, you have another USD 50000 to get that rate and vice versa.</p>
		          <p><strong>2) Booking heavy when the domestic currency is depreciated:</strong> If the domestic currency (INR) has depreciated a lot (more than 2% to 5%) recently then you should book your upcoming & future exposure as well. For Example, if the USD/INR pair has recently moved to 75 from 72 Rs. In this case, the domestic currency INR has depreciated by almost 4% and is supposed to appreciate in the future or will depreciate with a marginal amount. So, it is advisable to book most of your exposure for up to six months in that scenario.</p>
		          <p><strong>3) Booking a little less than your expected payment:</strong> If you expect a payment of USD 100000, you will most likely receive a little less because of correspondent bank charges. You may receive an amount of USD 99950 in your bank’s Nostro account. So if you are planning to book the entire exposure then always book USD 500 less than your expected payment to avoid cancellation of extra amount and charges thereon.</p>
		          <p><strong>4) Check the exact premium before booking a currency forward contract:</strong> You should check the exact premium before booking a contract so that you get exactly what is available in the market. You may refer to currency websites like <a href="https://www.ibrlive.com">ibrlive.com</a> which displays the live forward rates.</p>
		          <p><strong>5) Always opt for one month window:</strong> For example, if you are booking a <a href="https://ibrlive.com/blogs/usd-to-inr-exchange-rate-by-settlement-date">USD to INR</a> forward contract for USD 100000 for maturity on 20.09.2021 then you should ask your bank to provide a complete month window. You will get a window of one month starting from 20.09.2021 to 19.10.2021. Please note that this window is provided because sometimes foreign payments can be delayed due to unforeseen reasons. You will get premium only till the start date i.e 20.09.2021 but all your inward payments up to USD 100000 from 20.09.2021 till 19.10.2021 can be converted on the booking rate.</p>
		          <p><strong>6) Never do booking more than your currency exposure:</strong> For example, if you are expecting a payment of USD 100000 and you are finding the rate very attractive, but never book more than your expected payment. Because no one can predict currency movements. In case of adverse movement, you may lose money for the cancellation of the extra booking.</p>
		          <p><strong>7) Book continuously:</strong> Booking continuously is another good practice to follow. Booking currency forward contracts regularly helps to balance your currency rate.</p>
		          <p><strong>8) Booking immediately after receiving the purchase order:</strong> One should book immediately a partial amount of the contract after receiving the purchase order. This helps you prevent losses on account of the adverse movement.</p>
		          <p><strong>9) Book for a shorter duration when expecting an appreciation in domestic currency:</strong> if you are expecting a depreciation in domestic currency in a short span then you should book contracts for a shorter duration like for 15 days to 1 month.</p>
		          <p><strong>10) Book for a longer duration when expecting a depreciation in domestic currency:</strong> you are expecting an appreciation in the domestic currency then you should book contracts for a longer duration like for 2 months to 6 months.</p>
