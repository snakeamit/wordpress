		      <h1 class="panel-title"><strong> How understanding of USD to INR CASH, TOM, SPOT & FORWARD rates can benefit an Exporter or Importer?</strong></h1> 
		      </div>
		      <div class="panel-body" style="font-family: Verdana, sans-serif; font-size: 14px;">
		          
		          <!--<h4 class="text-center"><strong>How understanding of USD to INR CASH, TOM, SPOT & FORWARD rates can benefit an Exporter or Importer?</strong></h4>-->
		          <img class="img-responsive center-block" style="width: 50%" src="https://ibrlive.com/images/blogs/inr-usd.png"/>
		          <br/>
		          <p>When it comes to USD to INR exchange, there are four types of exchange rates differentiated by settlement date. With an understanding of these rates, you can save a lot of money & hedge your currency exposure. Let us understand one by one in detail.</p>
		          <br/>
		          <h4><strong>1. Spot Rate</strong></h4>
		          <p>When buying or selling of a currency happens on the same day but the delivery of the currency happens on the third day (on a T+2 Basis), then the rate is called a spot rate.</p>
		          
		          <p>All types of currency exchange rates quoted by banks are called spot rates and the rate you see on ibrlive.com or any other currency platform is a spot rate. Further, the spot rates are quoted by banks as BID & ASK rates. Where BID is the buy rate on which the bank purchases foreign currency from the client (generally exporters) and ASK is the sell rate on which the bank sells foreign currency to the client (generally Importers). In both the rates, there is a difference of some pips which is called a spread. So all the clients must look at BID & ASK rates while converting their currencies through banks.</p>
		          
		          <p>Example from exporter’s perspective: Suppose today is 5thApril 2021 and Monday. Any spot rate booked today will be delivered on a T+2 basis. Where T is the trading day i.e.5thApril 2021 and 2 days are counted as Tuesday and Wednesday. So the delivery of the currency will happen on Wednesday 7thApril 2021. So an exporter booking spot rate on 5thApril 2021 for his inward remittance will get the credit in his account on 7thApril 2021.</p>
		          <p>Example from importer’s perspective: Suppose today is 5thApril 2021 and Monday. Any spot rate booked today will be delivered on T+2 basis. Where T is the trading day i.e.5thApril 2021 and 2 days are counted as Tuesday and Wednesday. So the delivery of the currency will happen on Wednesday 7thApril 2021. So an importer booking spot rate on 5thApril for his outward payment then his supplier will get the payment on 7thApril and the money will be deducted from his current account on 7thApril 2021.</p>
		          <p><strong>Benefit of Booking Spot Rate to Exporter</strong></p>
                  <p>If an exporter books spot rate, then he may save his money by 2 to 3 paisa margin as he will not have to pay the cash spot difference margin. So if he does not immediately want the money or can wait for 2 days then he should book a spot rate.</p>
                  
                  <p><strong>Benefit of Booking Spot Rate to Importer</strong></p>
                  <p>If an importer has to make a payment after two days to his supplier but he finds the rate available today very attractive and projects that the rate can go up after two days, so he books a spot rate on 5thApril 2021 and make the payment on 7thApril 2021.</p>
                  
                  <br/>
                  <h4><strong>2. Cash Rate</strong></h4>
		          <p>When the trade and delivery of currency happen on the same day then it is called a cash rate or cash transaction.</p>
		          <p>If an exporter got his inward remittance in his bank and he books a cash rate for the same, then he has to sacrifice some additional margin on his currency exchange but he gets the payment in his account the same day.</p>
		          <p>Example from exporter’s perspective: Spot rate (BID) quoted by the bank to his exporter client today on 5thApril 2021 is 74.00 and the cash spot difference is 2 paisa on 5thApril 2021. If he books the Cash rate for $100000, then he will get 73.98 and the money will be credited to his bank on the same day with Rs. 7398000/-. If he would have booked the spot rate, then the money would have credited to his bank account on 7thApril 2021 with Rs. 7400000/-. So he paid Rs. 2000 to get the money on the same day by booking a cash rate. This cash spot difference gets increased if there is a holiday between trade day and settlement day.</p>
		          <p>Example from importer’s perspective: Spot rate (ASK) quoted by the bank to his exporter client today on 5thApril 2021 is 74.00 and the cash spot difference is 2 paisa on 5thApril 2021. If he books the cash rate for $100000, then he will get the cash rate as 73.98. His account will get debited on the same day with Rs. 7398000. So he saved Rs. 2000 by booking cash rate instead of the spot rate.</p>
		          
		          <p><strong>Benefit of Booking Cash Rate to Exporters</strong></p>
                  <p>If an exporter has to make urgent payments to his suppliers or has to avail cash discount on the purchase of his raw material and having a very good deal which can last today only, then he should book the cash rate.</p>

                  <p><strong>Benefit of Booking Cash Rate to Importers</strong></p>
                  <p>Importers get the benefit of cash spot difference which is deducted from the spot rate and they have to pay a lesser amount for conversion of his currency instead of booking the spot rate. This is a lesser-known fact as a majority of small importers treat the spot rate as cash rate and they do not claim the benefit of cash spot difference.</p>

                  <br/>
                  <h4><strong>3. Tom Rate</strong></h4>
		          <p>TOM means tomorrow. Settlement of TOM rate is done on a T+1 basis. Any exporter booking a TOM rate will have to pay a margin that is lesser than the cash spot difference and his account will be credited on the next working day. Similarly, any Importer booking TOM rate will get some benefit of TOM SPOT difference and his account will be debited on next day.</p>
		          <br/>
		          
		          <h4><strong>4. Forward Rate</strong></h4>
		          <p>A forward contract is a contract between two parties buying or selling a specific currency at a predefined rate for settlement on a specific future date.</p>
		          <p>The forward rate can be booked after the spot date (T+2 days) i.e. from T+3 to any future date. Any currency can be on premium or discount in comparison to another currency. For instance, USD EUR and GBP are three major currencies that are available at a premium in comparison to INR. For selling USD to INR on a future date an exporter gets a premium and for buying USD/ INR an importer has to pay the premium. Generally, the forward premium is calculated as an interest rate differential between two countries. You can get the exact forward premiums on <a href="https://ibrlive.com">ibrlive.com</a></p>
		          <p>Example from exporter’s perspective: Suppose today is 5thApril 2021 and the Spot exchange rate is 74.00. An exporter receives an order for USD 100000 for which payment is expected after 90 days from the date of order i.e. on 5thJuly 2021. He is of the view that the rate can go down significantly on the date of receipt of payment. So, the exporter gives the order copy to his bank and asks to book a forward contract for 5thJuly 2021. Bank quotes a premium of around 80 paisa for 90 days and gives a net rate of 74.80 for his payment expected on 5thJuly 2021. Bank allows one-month window to get the same rate on his currency if the payments come between any day from 5thJuly 2021 to 4thAugust 2021. The exporter will get Rs. 7480000 in his bank account if his inward remittance comes between 5th July 2021 to 4thAugust 2021 even if the current spot rate on the date of payment is 73.00 or 75.00.</p>
		          <p>Example from Importer’s perspective: Suppose today is 5thApril 2021 and the Spot exchange rate is 74.00. An importer enters into a contract with his supplier to deliver the goods on payment of 90 days. He is of the view that the rate can increase significantly on the 5thJuly 2021 on the date of payment.  So he asks his bank to book a forward contract for 5thJuly 2021. His bank quotes a premium of 82 paisa for 90 days and gives a net rate of 74.82. The importer would pay Rs. 7482000 for his currency exchange on 5thJuly 2021 even if the current rate on 5thJuly 2021 is 73.00 or 75.00.</p>
		          
		          <p><strong>Benefits of Booking Forward Contract for Exporter</strong></p>
		          <ol type="a">
                    <li>Exporter gets the benefit of premium on selling the currency on a future date.</li>
                    <li>Hedging from the risk of currency fluctuation.</li>
                  </ol>
                  
                  <p><strong>Benefits of Booking Forward Contract for Importer</strong></p>
		          <ol type="a">
                    <li>The importer has to pay the premium to book a forward rate but he also hedges his currency fluctuation risk by booking a forward contract.</li>
                  </ol>

                  <p>By understanding different types of exchange rates by their settlement dates an Importer or Exporter can manage his funds in a better way and save a lot of money.</p>
