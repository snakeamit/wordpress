		          <h4><strong>What is an EEFC account?</strong></h4>
	
		          <p>Exchange Earners Foreign Currency Account is called an EEFC account. An EEFC account is like a current account maintained in foreign currency with a bank authorized to deal in foreign exchange. However, there is no interest given on this type of account. </p>
		          <p>All earnings of a resident Indian can be held in an EEFC account to avoid conversions in Indian Rupee, and transaction costs. For Example, An Exporter may keep his USD inward receipts in EEFC accounts and make payment for his import payments in USD currency directly from the EEFC account, and avoid dual conversion cost.</p>
		          <br/>
		          
		          <h4><strong>Who is eligible to open an EEFC account?</strong></h4>
		          <p>Any Indian resident individual, company, a proprietorship firm, and partnership firm which earns money in foreign currency can open an EEFC account.</p>
		          <br/>
		          
		          <h4><strong>Can units in Special Economic Zone open an EEFC Account?</strong></h4>
		          <p>Units in special economic zones are generally not allowed to open EEFC accounts, but they can open EEFC accounts subject to certain conditions mentioned in <a href="https://www.rbi.org.in/Scripts/NotificationUser.aspx?Id=10261&Mode=0" target="_blank">FEMA Regulations</a> dated 21.01.2016.</p>
                  <br/>
                    
		          <h4><strong>What are the permitted debits from the EEFC account?</strong></h4>
		          <ol type="1">
                    <li>Payment outside India towards a permissible current account and capital account transaction like for import of goods & services or for investing abroad.</li>
                    <li>Payment in foreign exchange towards the cost of goods purchased from a 100 % EOU or a Unit in Export Processing Zone or Software Technology Park or Electronic Hardware Technology Park.</li>
                    <li>Payment of customs duty.</li>
                    <li>Remittance of Trade-related loans and advances given by an exporter to its importer customer outside India.</li>
                    <li>Payment in foreign currency to a person resident in India for the supply of goods/services including payments for airfare and hotel expenditure.</li>
                  </ol>
                  <br/>
                  
		          <h4><strong>For how long can the foreign currency be kept in the EEFC account?</strong></h4>
		          <p>Total funds credited in EEFC account during a calendar month must be converted into INR on or before the last day of the succeeding calendar month after adjusting for utilization of the balances for approved purposes or forward commitments. For Example if the funds are credited in EEFC account on any day during the month of October 2021, then these funds are necessarily to be converted into Indian Rupee on or before 30th November 2021.</p>
		          <br/>
		          
		          <h4><strong>What are the benefits of EEFC account for exporters?</strong></h4>
		          <ol type="1">
                    <li>Natural Hedge: An exporter who is also engaged in the import of goods or services can keep his export remittances in the EEFC account and make import payments from the EEFC account, thus getting a natural <a href="https://ibrlive.com/blogs/usd-to-inr-exchange-rate-by-settlement-date">hedge</a> on foreign exchange.</li>
                    <li>Saving of conversion cost: Making import payments or payments for fairs or commissions from the EEFC account can save currency conversion costs from both sides.</li>
                    <li>Benefit of currency forecast: An exporter who predicts that the foreign currency may appreciate in comparison to domestic currency in near future can hold his funds in EEFC accounts and convert it later at a higher price.</li>
                    <li>Hedging of EEFC funds: Funds held in EEFC accounts can be hedged by <a href="https://ibrlive.com/blogs/powerful-strategies-booking-currency-forward-contract-for-exporters">booking forward contact</a> and premium can be earned.</li>
                    <li>Payment of FCTL interest and principal: - Payment of interest and principal of foreign currency term loan can be made from EEFC account making it a less risky proposition</li>
                  
