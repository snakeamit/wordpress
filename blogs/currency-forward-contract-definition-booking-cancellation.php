		          <h4><strong>What Is a Currency Forward Contract?</strong></h4>
		          <p>A currency forward contract can be defined as buying or selling a specific currency at a specified future price for delivery on a specified future date.</p><br/>
		          
		          <h4><strong>Who Can Book a Currency Forward Contract?</strong></h4>
		          <p>Any importer or exporter having exposure to foreign currency can book a forward contract with its bank based on underlying (purchase order or pro forma invoice) to hedge his currency movement risk.</p><br/>
		          
		          <h4><strong>How to Book a Currency Forward Contract?</strong></h4>
		          <p><strong>Step 1:</strong> On request bank set up a forward booking limit on behalf of its client. To set up the forward booking limit bank demands a fixed deposit of approx. 5% of your total booking requirements in the INR term. For example, if you want to book USD100000 then you will have to produce a fixed deposit of Rs. 375000.00 to your bank (considering USD/INR at 75.00). If you are a credit customer then the bank may also set up the limit based on your collateral mortgaged with the bank.</p>
		          <p><strong>Step 2:</strong> You must produce an underlying (valid purchase order or pro forma invoice) mentioning the delivery and payment terms before your bank to book a currency forward contract.</p>
		          <p><strong>Step 3:</strong> Bank gives you a forward rate and with your consent, book the same. For example, if you want to book USD 100000 for delivery after the end of 3 months. Considering the current USD to INR spot rate at 75.00, the bank may give you a forward rate of 75.90. Here 0.90 is the premium for three months. Please note that exporters get the benefit of premium and importers have to pay the premium because USD is almost on premium with comparison to INR. You may refer to <a href="https://ibrlive.com">ibrlive.com</a> for live forward rates.</p>
		          <p><strong>Step 4:</strong> On successful booking of the contract bank agrees, generally on a 100 Rs. Stamp paper. The agreement contains all details of the contract and it is signed by both bank and client.</p><br/>
		          
		          <h4><strong>How Currency Forward Contracts Are Utilized?</strong></h4>
		          <p>On successful arrival of payment against export or sending the payment for import on the maturity date of forwarding contract, the bank gives you the same rate which was booked earlier under forwarding contract irrespective of the current spot rate on the maturity date.</p>
		          <p>A forward contract can be utilized for other payments irrespective of the underlying (purchase order or pro forma invoice) on behalf of which it was booked.</p>
		          <p>Early utilization of forwarding contracts is also possible if your payment has come earlier than the expected date.</p><br/>
		          
		          <h4><strong>Where to Check Exact Forward Premiums and Forward Rates?</strong></h4>
		          <p>Many websites show the month-wise or broken date forward rates for a subscription basis. You may refer to ibrlive.com for knowing exact premiums and final forward rate, even for broken dates.</p><br/>
		          
		          <h4><strong>Advantages of Currency Forward Contract</strong></h4>
		          <p>For importers & exporters, the main advantage of booking a currency forward contract is to hedge their foreign currency exposure from adverse movements. Exporters booking a forward contract for USD to INR, EUR to INR, or GBP to INR or any major currency get the benefit of premium added to the present spot rate.</p><br/>
		          
		          <h4><strong>Can I Cancel a Forward Contract?</strong></h4>
		          <p>Yes, the forward contract can be cancelled on the maturity date or 3 days after the maturity date. Cancellation is done on spot rate and any profit or loss will be passed on to the customer if the same is cancelled on or before the maturity date.</p>
		          <p>If the forward is cancelled any day between 3 daysâ€™ grace period, then the profit will not be passed on to the customer but any loss will be recovered from his account.</p>
		          <p>A forward contract can also be cancelled before the maturity date. Apart from profit & loss calculation from spot day, the client will also have to forgo extra premium from the date of cancellation to maturity.</p>

